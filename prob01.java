// 1. Take 2 integer values in two variables x and y and print their product.
// Sample Input : x=2, y=4 (Both integers)
// Sample Output : 8
public class prob01 {
    public static void main(String[] args) {
        int x, y, product;
        x = 2;
        y = 4;
        product = x * y;
        System.out.println("product of x & y = " + product);
    }
}
